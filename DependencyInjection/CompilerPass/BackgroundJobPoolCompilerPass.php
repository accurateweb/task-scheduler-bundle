namespace Accurateweb\TaskSchedulerBundle\DependencyInjection\CompilerPass;

use Symfony\Component\DependencyInjection\Compiler\CompilerPassInterface;
use Symfony\Component\DependencyInjection\ContainerBuilder;
use Symfony\Component\DependencyInjection\Reference;

class BackgroundJobPoolCompilerPass implements CompilerPassInterface
{
  public function process (ContainerBuilder $container)
  {
    $jobs = $container->findTaggedServiceIds('aw.schedulable_task');
    $pool = $container->getDefinition('aw.task_scheduler.background_job_pool');

    foreach ($jobs as $id => $tags)
    {
      $pool->addMethodCall('addBackgroundJob', [new Reference($id)]);
    }
  }
}